// Arithmetic Operators
let additionResult = 5 + 3; // additionResult is 8
let subtractionResult = 5 - 3; // subtractionResult is 2
let multiplicationResult = 5 * 3; // multiplicationResult is 15
let divisionResult = 10 / 2; // divisionResult is 5
let modulusResult = 10 % 3; // modulusResult is 1
let x = 5;
x++; // x is now 6
let y = 5;
y--; // y is now 4

// Assignment Operators
let xValue = 5;
xValue += 3; // xValue is now 8
let yValue = 10;
yValue -= 5; // yValue is now 5

// Comparison Operators
let isEqual = (5 == 5); // isEqual is true
let isNotEqual = (5 != 3); // isNotEqual is true

// Logical Operators
let logicalAndResult = (true && false); // logicalAndResult is false
let logicalOrResult = (true || false); // logicalOrResult is true
let logicalNotResult = !true; // logicalNotResult is false

// Ternary Operator
let ternaryResult = (x > 0) ? 'positive' : 'non-positive';

// Bitwise Operators
let bitwiseAndResult = 5 & 3; // bitwiseAndResult is 1
let bitwiseOrResult = 5 | 3; // bitwiseOrResult is 7
let bitwiseXorResult = 5 ^ 3; // bitwiseXorResult is 6

// Unary Operators
let unaryPlusResult = +(-5); // unaryPlusResult is -5
let unaryMinusResult = -(5); // unaryMinusResult is -5

// Grouping Operators
let groupingResult = (5 + 3) * 2; // groupingResult is 16

// Comma Operator
let commaResult = (2, 3); // commaResult is 3

